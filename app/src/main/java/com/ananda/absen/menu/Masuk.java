package com.ananda.absen.menu;

import java.util.Date;

class Masuk {
    public Date tanggal_masuk;
    public Date tanggal_masuk_hari;
    public Date jam_masuk;

    public Date getTanggal_masuk() {
        return tanggal_masuk;
    }

    public void setTanggal_masuk(Date tanggal_masuk) {
        this.tanggal_masuk = tanggal_masuk;
    }

    public Date getTanggal_masuk_hari() {
        return tanggal_masuk_hari;
    }

    public void setTanggal_masuk_hari(Date tanggal_masuk_hari) {
        this.tanggal_masuk_hari = tanggal_masuk_hari;
    }

    public Date getJam_masuk() {
        return jam_masuk;
    }

    public void setJam_masuk(Date jam_masuk) {
        this.jam_masuk = jam_masuk;
    }
}
